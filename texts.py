    def fmt(self, data):
        if 'text' in data:
            text = data['text']

            if text['from'] == 'getCategories':
                if text['data']['exceeded'] == True:
                    return self.present_failure_limit_exceeded
                elif not text['data']['available_categories']:                                                                            return self.present_failure_amount
                else:
                    return self.choose_category

            elif text['from'] == 'getPresentsList':
                res = ""
                for row in text['data']:
                    res += row['present_in_category'] + '\n'
                return self.getPresentsList + res
                
            elif text['from'] == 'getPresent':
                return self.present_success + text['data']['present_in_category']

